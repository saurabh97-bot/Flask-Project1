# Steps to setup Flask Project.

# 1. Setting up project directory
# 2. Open the project directory in cmd
# 3. Activate vscode
# 4. Create a virtual Environment.
# Command: python -m venv [foldername]
# 5. Activate Virtual Environment
# Command: env\Scripts\activate

# 6. Install Flask
# Command: pip install flask

# 7. Create the Flask Application

# 8. Run the Application
# command: python app.py (Python File)

# Flask Dev Server: 127.0.0.1:5000